function check(id) {
    return new Promise((resolve) => {
        const url = `https://images.unsplash.com/photo-${id}?w=100&h=100&fit=crop&q=50`;
        https.get(url, (res) => { resolve(res.statusCode); res.resume(); })
             .on('error', () => resolve('err'));
    });
}
